package com.centralmandados.model;

/**
 *
 * @author Laris
 */
public class Oficial {
    private int id;
    private String nome;

    public Oficial() {
    }
    
    public Oficial(int id) {
        this.id = id;
    }

    public Oficial(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public String toString() {
        return nome;
    }
}
